/**
*reverse_list - Reverses a singly-linked listint_t list.
*@head: A pointer to the starting node of to list to reverse.
*
*Return: A pointer to the head of the reversed list.
*/
void reverse_listint(listint_t **head)
{
	listint_t *prev = NULL;
	listint_t *current = *head;
	listint_t *next = NULL;

	while (current)
	{
		next = current->next;
		current->next = prev;
		prev = current;
		current = next;
	}
	*head = prev;
}
